    <p>В беседе с Cointelegraph генеральный директор Hive Digital Technologies Айдын Килич и председатель Фрэнк Холмс подтвердили, что компания по-прежнему активно занимается биткойнами и крипто-майнингом, несмотря на смену названия.</p>
