    <h3>Crypto miner Hive Digital</h3>
    <p>Криптомайнинговая компания Hive Blockchain подала сигнал технологической индустрии, когда в июле она отказалась от названия “блокчейн” своего тезки, решив называть себя Hive Digital Technologies и отражать свой набег на искусственный интеллект.</p>
    <p>Это решение является частью более широкого движения от крипто-майнеров к выходу за рамки майнинга цифровой валюты на фоне снижения цен на криптовалюту. В январе криптомайнерская компания Riot Blockchain провела ребрендинг Riot Platforms, а в июне криптомайнер Applied Digital объявил о сделке на сумму 460 миллионов долларов на размещение облачных вычислений AI в своем центре обработки данных.</p>
    <div class="note">
        <p>С тех пор, как майнинг ETH завершился в сентябре 2022 года, $ HIVE преобразовывает свой парк из 38 000 графических процессоров Nvidia из майнеров в облачную инфраструктуру, отмечая следующий этап нашей эволюции в оператора диверсифицированного центра обработки данных.</p>
