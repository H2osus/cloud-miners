    <p>“ИИ позволяет Web3 быть более адаптивным. В Web2 у вас есть все эти метаданные, но вы теряете конфиденциальность, потому что все крупные интернет-компании тайно собирают все эти данные для получения прибыли”, - сказал Килич.</p>
